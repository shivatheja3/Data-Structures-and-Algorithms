class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(vector<int>arr, int n, long long s)
    {
        long long sum1=0;
        int p1=0,p2=0;
        if(s==0){
            return {-1};
        }
        while(p1<n){
            // cout<<sum1<<" "<<p1<<" "<<p2<<endl;
            if(sum1==s)
                return {p1+1,p2};
            if(sum1<s&&p2<n)
                sum1+=arr[p2++];
            else{
            sum1-=arr[p1++];
            }
        }
        return {-1};
    }
};

